/* ----------------------------- Loose equal == ----------------------------- */
// Untuk membandigkan apakah kedua value itu setara/sama
console.log(10 == 10);
console.log(5 == 5);
console.log(1 == "ABC");
console.log(1 == "1");

/* ---------------------------- Strict equal === ---------------------------- */
console.log(10 === 10);
console.log(5 === 5);
console.log(1 === "ABC");
console.log(1 === "1");

